# tuple basic
# tuple is immutable
# tuple is faster than list
# tuple can be used as key in dictionary
# tuple can be used as element in set
# tuple can be used as argument in function
# tuple can be used as return value in function
# tuple can be used as slice index
# tuple can be used as slice range
# tuple can be used as slice step
# tuple can be used as slice start, stop, step

# example
tulpe_x = (1, 2, 3, 4, 5, 6, 7, 8, 9)
print(f"tulpe_x[0]): ", tulpe_x[0])

tulpe_x[1] = 10  # TypeError: 'tuple' object does not support item assignment
tuple_x = (1, 2, 2)
print(f"tuple_x.count(2): ", tuple_x.count(2))
